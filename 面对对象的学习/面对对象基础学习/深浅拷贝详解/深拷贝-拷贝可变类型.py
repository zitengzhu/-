'''
案例：深浅拷贝

记忆：
    1.所谓的深浅拷贝分别指的是：
        浅拷贝：copy模块的copy()
        深拷贝：copy模块的deepcopy()
    2.大白话解释深浅拷贝：
        深拷贝：拷贝的多
        浅拷贝：拷贝的少
    3.深浅拷贝主要是针对于可变类型来讲的，深拷贝拷贝所有(可变的)层，浅拷贝之拷贝(可变的)第一层
      如果针对不可变类型，则深浅拷贝的用法的用法和普通赋值一样

'''

'''
copy.deepcopy() 创建的是深拷贝，意思是：
创建一个新的外层容器（新的列表）
同时递归创建所有内部元素的副本
关键特点：
外层容器是新的 - id(c) ≠ id(d)
内部元素也是新的 - id(c[2]) ≠ id(d[2])
完全独立 - 修改任何一方都不影响另一方
'''

import copy


def demo():
    a = [1,2,3]
    b = [11,22,33]
    c = [6,7,a,b]

    d=copy.deepcopy(c)
    print('id(c)-->',id(c))
    print('id(d)-->', id(d))

    a[1]=100
    b[1]=800
    print(f'c',c)
    print(f'd',d)

demo()

# id(c)--> 1979955478656
# id(d)--> 1979955478720
# c [6, 7, [1, 100, 3], [11, 800, 33]]
# d [6, 7, [1, 2, 3], [11, 22, 33]]

# 由上述结果可知 c和d的地址值不一样
# 深拷贝创建的是新的列表 两个列表相互独立所以地址值不一样
# 且深拷贝创建的列表与浅拷贝不同的是深拷贝拷贝的内部元素也是独立的
# 不会出现跟浅拷贝那样地址值不同但数据不独立的情况（修改一个另一个也变）

