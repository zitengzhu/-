'-------------------------运算符----------------------------'
# 运算符 % (求余，即返回除法的余数)
print(7%2)  # 1
# 注： 使用%求余数，如果除数（第二个数字）是负数，那么取得的结果也是一个负值   用%求余时除数不能是0不然会报错
print(7%-2)  # -1

# 运算符 // (取整数，即返回商的整数部分)
print(7//2)  # 3
# 注： 使用除法（/和//）运算符时，除数（第二个数字）不能为0不然会报错

# 运算符 ** (幂，即返回x的y次方)
print(2**4)  # 16  2的四次方
